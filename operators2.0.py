#logical operators
#AND OPERATORS
x=30
print(x>20 and x>10)

print(x>20 and x<5)
 
print(x<30 and x>20)

print(x<30 and x>35)

#NOT OPERATORS
x=30
print(not(x>20 and x>10))

print(not(x>20 and x<5))

print(not(x<30 and x>20))

print(not(x<30 and x>35))

#OR OPERATORS
x=30

print(x>20 or x>10)

print(x>20 or x<25)

print(x<5 or x>20)

print(x>35 or x>55)

